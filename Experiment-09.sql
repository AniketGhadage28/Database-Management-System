/* TRIGGERS */
CREATE TABLE CARS (CAR_NAME VARCHAR2(100))

INSERT INTO CARS VALUES(' HYUNDAI I10 ')
INSERT INTO CARS VALUES(' SUZUKI ERTIGA ')

CREATE OR REPLACE TRIGGER CAR_TRIM  
BEFORE INSERT ON CARS  
FOR EACH ROW  
BEGIN  
 :NEW.CAR_NAME := TRIM(:NEW.CAR_NAME);  
END;
/*Trigger created. */ 

